package Tabla;

public class dragon {
	String per;
	String pod;
	public dragon() {
		
	}
	
	public dragon(String per, String pod) {
		super();
		this.per = per;
		this.pod = pod;
	}

	public String getPer() {
		return per;
	}
	public void setPer(String per) {
		this.per = per;
	}
	public String getPod() {
		return pod;
	}
	public void setPod(String pod) {
		this.pod = pod;
	}
	

}
